/** Get week total BP */
export function getWeekBP(week, override = null) {
  if (override) return override;
  const wi = STORE_BP[week];
  if (!wi) return null;
  return wi.reduce((a, b) => a + b, 0) * 10;
}

/** Auto-detect current week number from date */
export function currentWeekNum() {
  const now    = new Date();
  const jan1   = new Date(now.getFullYear(), 0, 1);
  const days   = Math.floor((now - jan1) / 86400000);
  return Math.ceil((days + jan1.getDay() + 1) / 7);
}

/** Map JS Date.getDay() → store day string (week starts Friday) */
export function todayDayStr() {
  const jsDay = new Date().getDay(); // 0=Sun...6=Sat
  const map   = { 0:'Sun', 1:'Mon', 2:'Tue', 3:'Wed', 4:'Thu', 5:'Fri', 6:'Sat' };
  return map[jsDay] || 'Mon';
}
